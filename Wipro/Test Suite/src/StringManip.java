public class StringManip {

	/**
	 * @param args
	 */
	
		String datum; 
		
		public StringManip(String datum) {
			this.datum = datum; 
		}
		
		public String upperCase() {
			return datum.toUpperCase();
		}
		public String lowerCase() {
			return datum.toLowerCase();
		}


}
